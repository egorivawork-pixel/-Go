package main

import "fmt"

func main() {
	массив := [5]int{10, 20, 30, 40, 50}
	fmt.Println("Исходный массив:", массив)

	срез := массив[0:5]
	fmt.Println("Исходный срез:", срез)

	срез = append(срез, 60, 70)
	fmt.Println("Срез после добавления элементов:", срез)

	индексУдаления := 3
	срез = append(срез[:индексУдаления], срез[индексУдаления+1:]...)
	fmt.Println("Срез после удаления элемента:", срез)
}
