// Задание 6: Программа вычисляет среднее значение трёх чисел.

package main

import "fmt"

func main() {
	// Три числа (можно поменять на любые)
	n1, n2, n3 := 7.0, 12.0, 3.0

	// Среднее арифметическое = сумма / количество
	avg := (n1 + n2 + n3) / 3

	fmt.Println("Числа:", n1, n2, n3)
	fmt.Println("Среднее значение:", avg)
}
